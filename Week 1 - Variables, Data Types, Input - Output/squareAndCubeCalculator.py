print("*-*-*-*-*-*-*- Square and Cube Calculator -*-*-*-*-*-*-*")
number = int(input("Please enter the number you want to calculate the square and cube of = "))

square = number * number
cube = number * number * number # With these two lines of code, we calculate both the square and the cube of the given number

print(f"Square of the number you entered = {square}")
print(f"Cube of the number you entered = {cube}") # Here we print the result to the terminal