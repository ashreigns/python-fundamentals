print("*-*-*-*-*-*- Celsius to Fahrenheit Converter -*-*-*-*-*-*-*")
celsius = int(input("Please enter the celsius degree you want to convert to Fahrenheit = "))

fahrenheit = (celsius * 9/5) + 32 # Here we found the fahrenheit equivalent of the given celsius degree using this formula

print(f"Result of your operation = {fahrenheit} fahrenheit.")